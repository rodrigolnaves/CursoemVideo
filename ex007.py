nota1 = float(input('Digite a primeira nota do aluno: '))
nota2 = float(input('Digite a segunda nota do aluno: '))
media = (nota1 + nota2) / 2
print('A primeira nota do aluno é: {:.2f}.\n'
      'A segunda nota do aluno é: {:.2f}.\n'
      'A média do aluno é: {:.2f}.'. format(nota1, nota2, media))
